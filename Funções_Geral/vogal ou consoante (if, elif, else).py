letra = input("Informe uma letra: ")
if letra == "a" or letra == "A":
    print ("Vogal")
elif letra == "a" or letra == "B":
    print ("Vogal")
elif letra == "a" or letra == "C":
    print ("Vogal")
elif letra == "a" or letra == "D":
    print ("Vogal")
elif letra == "a" or letra == "E":
    print ("Vogal")

else:
    print ("Consoante")